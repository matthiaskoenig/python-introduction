"""Module with helper functions."""

def simple_sum(x, y):
	"""Compute sum of two numbers.

	The function computes the sum of two numbers
	usage: z = simple_sum(x,y)
	"""
	z = x + y
	return z

def simple_product(x, y):
	"""Compute product of two numbers.

	The function computes the product of two numbers
	usage: z = simple_product(x,y)
	"""
	z = x * y
	return z
